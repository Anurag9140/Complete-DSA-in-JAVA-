

public class Basic_Loops {
    public static void main(String[] args) {
        for(int i=19;i<=190;i+=19){
            System.out.println(i);
        }

        for(int i=1;i<=10;i++){
            System.out.println("ANURAG VERMA");
        }

        // Print all Even Numbers btw 1 to 100
        for(int i=2;i<=100;i+=2){
            System.out.print(i+" ");
        }

        // Decreasing Loops
        for(int i=10;i>=1;i--){
            System.out.print(i+" ");
        }
    }

}
